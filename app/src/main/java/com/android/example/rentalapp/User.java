package com.android.example.rentalapp;

public class User {

    private String Name, Email, City, ContactNo;

    public User() {
    }

     public String getContactNo() {
        return ContactNo;
    }

     public String getName() {
        return Name;
    }

     public String getEmail() {
        return Email;
    }

     public String getCity() {
        return City;
    }
}
